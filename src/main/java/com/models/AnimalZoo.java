package com.models;


public class AnimalZoo {
    private String common_name;
    private String zoo_name;
    private int animal_count;

    public String getCommon_name() {
        return common_name;
    }

    public void setCommon_name(String common_name) {
        this.common_name = common_name;
    }

    public String getZoo_name() {
        return zoo_name;
    }

    public void setZoo_name(String zoo_name) {
        this.zoo_name = zoo_name;
    }

    public int getAnimal_count() {
        return animal_count;
    }

    public void setAnimal_count(int animal_count) {
        this.animal_count = animal_count;
    }
}
